console.log("Simple comparisons ");

console.log(2 > 1);
console.log(2 >= 1);
console.log(2 < 1);
console.log(2 == 1);
console.log(2 != 1);

// Different datatypes

console.log("Different Datatypes ");
console.log("2" > 1);
console.log("02" > 1);

// Null

console.log("Null : ");
console.log(null > 0);
console.log(null == 0);
console.log(null >= 0);

// Undefined 

console.log("Undefined: ");
console.log(undefined == 0);
console.log(undefined > 0);
console.log(undefined < 0);

// === Strict Equality 

console.log("Strict Check");
console.log("2" === 2);